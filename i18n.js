// `negated` covers geocaching.com's crossed-out "no" icon variant (data.json
// stores these as e.g. "!wheelchair") — same label, with a short suffix
// instead of a whole separate translated string per attribute.
function attributeLabel(slug, negated) {
  const key = CACHE_ATTRIBUTE_KEYS[slug];
  const label = key ? t(key) : slug;
  return negated ? label + ' (' + t('attrNotSuffix') + ')' : label;
}

function t(key, vars) {
  const dict = STRINGS[I18N_LANG] || STRINGS[I18N_DEFAULT_LANG];
  let str = dict[key] != null ? dict[key] : STRINGS[I18N_DEFAULT_LANG][key];
  if (str == null) return key;
  if (vars) {
    for (const k in vars) str = str.replace('{' + k + '}', vars[k]);
  }
  return str;
}

function typeLabel(ty) {
  const key = CACHE_TYPE_KEYS[ty];
  return key ? t(key) : ty;
}

function sizeLabel(sz) {
  const key = CACHE_SIZE_KEYS[sz];
  return key ? t(key) : '';
}

// Applies data-i18n / data-i18n-placeholder / data-i18n-aria-label to every
// matching element — run once on load, and again after switching I18N_LANG.
function applyStaticI18n() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('[data-i18n-aria-label]').forEach(function(el) {
    el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria-label')));
  });
}
